package com.callor.hello.prime;

public class PrimeC {
	public static void main(String[] args) {
		int num = 7;
		
		for (int i = 2; i < num; i++) {
			boolean isPrime = true;
			if(num % i == 0) {
				isPrime = false;
			}
			else {
				isPrime = true;
			}

		}
		System.out.println();
				
			
		}
				
	}				
				
				
			
		
		

